<template>
  <div class="p-2 flex flex-col md:flex-row md:text-left relative">
    <IconButton class="absolute top-0 right-0 p-3 text-2xl flex-none leading-none" icon="carbon:close" @click="$emit('close')" />
    <div :style="{color: previewColor}">
      <ColorPicker v-model:value="previewColor" class="inline-block">
        <Icon class="p-4 text-8xl" :icon="icon" />
      </ColorPicker>
    </div>
    <div class="px-6 py-2 mb-2 md:px-2 md:py-4">
      <a class="text-gray-500 hover:text-primary text-sm dark:text-dark-500" href="https://iconify.design/docs/iconify-in-pages/" target="_blank">
        How to use the icon?
      </a>
      <p class="flex text-gray-700 font-mono dark:text-dark-900">
        {{ icon }}
        <IconButton icon="carbon:copy" class="ml-2" @click="copy('id')" />
      </p>

      <p v-if="showCollection && collection" class="flex mb-1 text-gray-500 text-sm">
        Collection:
        <router-link
          class="ml-1 text-gray-600 hover:text-gray-700 dark:text-gray-300 dark-hover:text-gray-200"
          :to="`/collection/${collection.id}`"
          @click="$emit('close')"
        >
          {{ collection.name }}
        </router-link>
      </p>

      <div>
        <div
          class="
            inline-block border border-gray-200 my-2 mr-2 font-sans pl-2 pr-3 py-1 rounded-full text-sm cursor-pointer hover:bg-gray-100
            dark:border-dark-200 dark-hover:bg-dark-200
          "
          :class="inBag(icon) ? 'text-primary' : 'text-gray-500'"
          @click="toggleBag(icon)"
        >
          <template v-if="inBag(icon)">
            <Icon class="inline-block text-lg align-middle" icon="carbon:shopping-bag" />
            in bag
          </template>
          <template v-else>
            <Icon class="inline-block text-lg align-middle" icon="carbon:add" />
            add to bag
          </template>
        </div>

        <div
          v-if="inBag(icon)"
          class="inline-block my-2 mr-2 font-sans pl-2 pr-3 py-1 rounded-full text-sm cursor-pointer hover:bg-gray-100 dark-hover:bg-dark-200"
          :class="selectingMode ? 'text-primary' : 'text-gray-500'"
          @click="toggleSelectingMode"
        >
          <Icon class="inline-block text-lg align-middle" icon="carbon:list-checked" />
          multiple select
        </div>
      </div>

      <div class="flex flex-wrap mt-2">
        <div class="mr-4">
          <div class="my-1 text-gray-500 text-sm">
            Snippets
          </div>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('html')">
            HTML
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('css')">
            CSS
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('svg')">
            SVG
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('pure-jsx')">
            JSX
          </button>
        </div>
        <div class="mr-4">
          <div class="my-1 text-gray-500 text-sm">
            Components
          </div>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('vue')">
            Vue
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('jsx')">
            React
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('tsx')">
            React<sup class="opacity-50 -mr-1">TS</sup>
          </button>
        </div>
        <div class="mr-4">
          <div class="my-1 text-gray-500 text-sm">
            Links
          </div>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('url')">
            URL
          </button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="copy('data_url')">
            Data URL
          </button>
        </div>
        <div class="mr-4">
          <div class="my-1 text-gray-500 text-sm">
            Download
          </div>
          <button class="btn small mr-1 mb-1 opacity-75" @click="download('svg')">SVG</button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="download('vue')">Vue</button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="download('jsx')">React</button>
          <button class="btn small mr-1 mb-1 opacity-75" @click="download('tsx')">React<sup class="opacity-50 -mr-1">TS</sup></button>
        </div>
      </div>
    </div>
    <Notification :value="copied">
      <Icon icon="mdi:check" class="inline-block mr-2 font-xl align-middle" />
      <span class="align-middle">Copied</span>
    </Notification>
  </div>
</template>

<script setup="props, {emit}" lang='ts'>
import copyText from 'copy-text-to-clipboard'
import FileSaver from 'file-saver'
import { ref, computed } from 'vue'
import { getIconSnippet, toComponentName } from '../utils/icons'
import { collections } from '../data'
import { selectingMode } from '../store'

declare const props: {
  icon: string
  showCollection: boolean
}
declare const emit: (...args: any) => void

export const copied = ref(false)

export { previewColor, toggleBag, inBag } from '../store'
export const copy = async(type: string) => {
  const text = await getIconSnippet(props.icon, type, true)
  if (!text)
    return

  copied.value = copyText(text)
  setTimeout(() => {
    copied.value = false
  }, 2000)
}

export const download = async(type: string) => {
  const text = await getIconSnippet(props.icon, type, false)
  if (!text)
    return

  FileSaver.saveAs(new Blob([text], { type: 'text/plain;charset=utf-8' }), `${toComponentName(props.icon)}.${type}`)
}

export const toggleSelectingMode = () => {
  selectingMode.value = !selectingMode.value
  if (selectingMode.value)
    emit('close')
}

export const collection = computed(() => {
  const id = props.icon.split(':')[0]
  return collections.find(i => i.id === id)
})
</script>
